package ca.chrischristakis.ssgl.font;

public class Letter
{
	int x, y, width, height, xOffset, yOffset, xShift;
	
	public Letter(int x, int y, int width, int height, int xOffset, int yOffset, int xShift)
	{
		this.x = x; this.y = y; this.width = width; this.height = height; this.xOffset = xOffset; this.yOffset = yOffset; this.xShift = xShift;
	}

}
